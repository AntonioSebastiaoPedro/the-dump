#include "../includes/thedump.h"

void	draw_char_pixel(t_cub *cub, int x, int y, int character, int color)
{
	static const unsigned char font[37][5] = {
		{0x7, 0x5, 0x5, 0x5, 0x7}, // 0
		{0x2, 0x6, 0x2, 0x2, 0x7}, // 1
		{0x7, 0x1, 0x7, 0x4, 0x7}, // 2
		{0x7, 0x1, 0x7, 0x1, 0x7}, // 3
		{0x5, 0x5, 0x7, 0x1, 0x1}, // 4
		{0x7, 0x4, 0x7, 0x1, 0x7}, // 5
		{0x7, 0x4, 0x7, 0x5, 0x7}, // 6
		{0x7, 0x1, 0x1, 0x1, 0x1}, // 7
		{0x7, 0x5, 0x7, 0x5, 0x7}, // 8
		{0x7, 0x5, 0x7, 0x1, 0x1}, // 9
		{0x7, 0x5, 0x7, 0x5, 0x5}, // A
		{0x6, 0x5, 0x6, 0x5, 0x6}, // B
		{0x7, 0x4, 0x4, 0x4, 0x7}, // C
		{0x6, 0x5, 0x5, 0x5, 0x6}, // D
		{0x7, 0x4, 0x7, 0x4, 0x7}, // E
		{0x7, 0x4, 0x6, 0x4, 0x4}, // F
		{0x7, 0x4, 0x5, 0x5, 0x7}, // G
		{0x5, 0x5, 0x7, 0x5, 0x5}, // H
		{0x7, 0x2, 0x2, 0x2, 0x7}, // I
		{0x1, 0x1, 0x1, 0x5, 0x7}, // J
		{0x5, 0x4, 0x6, 0x5, 0x5}, // K
		{0x4, 0x4, 0x4, 0x4, 0x7}, // L
		{0x5, 0x7, 0x5, 0x5, 0x5}, // M
		{0x6, 0x5, 0x5, 0x5, 0x5}, // N
		{0x7, 0x5, 0x5, 0x5, 0x7}, // O
		{0x7, 0x5, 0x7, 0x4, 0x4}, // P
		{0x7, 0x5, 0x5, 0x7, 0x1}, // Q
		{0x7, 0x5, 0x6, 0x5, 0x5}, // R
		{0x7, 0x4, 0x7, 0x1, 0x7}, // S
		{0x7, 0x2, 0x2, 0x2, 0x2}, // T
		{0x5, 0x5, 0x5, 0x5, 0x7}, // U
		{0x5, 0x5, 0x5, 0x5, 0x2}, // V
		{0x5, 0x5, 0x5, 0x7, 0x5}, // W
		{0x5, 0x5, 0x2, 0x5, 0x5}, // X
		{0x5, 0x5, 0x2, 0x2, 0x2}, // Y
		{0x7, 0x1, 0x2, 0x4, 0x7}, // Z
		{0x1, 0x2, 0x4, 0x2, 0x1}  // /
	};
	int i, j;
	int char_idx;

	if (character >= '0' && character <= '9')
		char_idx = character - '0';
	else if (character >= 'A' && character <= 'Z')
		char_idx = character - 'A' + 10;
	else if (character >= 'a' && character <= 'z')
		char_idx = character - 'a' + 10;
	else if (character == '/')
		char_idx = 36;
	else
		return ;

	for (i = 0; i < 5; i++) {
		for (j = 0; j < 3; j++) {
			if ((font[char_idx][i] >> (2 - j)) & 1) {
				for (int dx = 0; dx < 3; dx++) // Scale 3x
					for (int dy = 0; dy < 3; dy++)
						pixel_put(cub, x + j * 3 + dx, y + i * 3 + dy, color);
			}
		}
	}
}

void	draw_string_graphics(t_cub *cub, int x, int y, char *str, int color)
{
	while (*str)
	{
		if ((*str >= '0' && *str <= '9') || (*str >= 'A' && *str <= 'Z') || (*str >= 'a' && *str <= 'z') || *str == '/')
		{
			draw_char_pixel(cub, x, y, *str, color);
			x += 15; // 3 blocks * 3 pixels + padding
		}
		else if (*str == ' ' || *str == ':' || *str == '-' || *str == '.')
			x += 10;
		str++;
	}
}